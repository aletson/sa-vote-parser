<h1>About ISBN2Dewey</h1>

<p>Hi there! I'm Andrew, the author of ISBN2Dewey.</p>

<p>I originally built this tool because I was moving to a new house and wanted to take the opportunity to reorganize my numerous books. I didn't want to sort by author, especially when I had a lot of topical nonfiction, so I decided I'd rather use the Dewey Decimal System. I looked around for a tool that would help me do this, since I didn't want to browse through databases manually to get the answers I was looking for.</p>

<p>Eventually, I hit on an idea - use the ISBN on a book to check the Dewey classification! I looked around for a tool to do this, and found the OCLC Classify API.</p>

<p>After successfully moving and reorganizing all of my books, I thought maybe my tool could be handy for more people, including real librarians, so I decided to make it publicly available. I hope you find it useful.</p>

<p>If you have any suggestions or comments, feel free to contact andrew+isbn2dewey@andrewletson.com.</p>

<p>If you've found the service useful, a small donation would go a long way towards keeping the service alive - I fund the domain and hosting out of my own pocket to keep it ad-free.</p>

